def multiply(v : list[int], A : list[list[int]]) -> list[int]:
    sum = [0] * len(A[0])
    for i in range(len(A[0])):
        for j in range(len(v)):
            sum[i] ^= v[j] & A[j][i]
    return sum

def syndrome(v : list[int], A : list[list[int]]) -> list[int]:
    return multiply(v, A)

